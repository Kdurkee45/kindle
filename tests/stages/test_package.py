def _make_state(project_dir: str, **overrides) -> dict:
    """Return a minimal KindleState dict for package_node()."""
    state: dict = {
        "project_dir": project_dir,
        "idea": "Build a CLI weather app",
        "feature_spec": {"name": "weather-cli", "features": ["current", "forecast"]},
        "architecture": "Python CLI with Click, requests, Rich",
        "dev_tasks": [
            {"id": 1, "title": "Setup project scaffolding"},
            {"id": 2, "title": "Implement weather API client"},
        ],
    }
    state.update(overrides)
    return state


def _make_agent_result(text: str = "Done") -> AgentResult:
    return AgentResult(
        text=text,
        tool_calls=[],
        raw_messages=[],
        elapsed_seconds=12.3,
        turns_used=5,
    )


def _setup_project_dir(tmp_path: Path) -> Path:
    """Create the minimal on-disk structure that artifacts helpers expect."""
    project_dir = tmp_path / "project"
    (project_dir / "artifacts").mkdir(parents=True)
    (project_dir / "logs").mkdir(parents=True)
    (project_dir / "workspace").mkdir(parents=True)
    # metadata.json is needed by mark_stage_complete / mark_project_done
    meta = {
        "project_id": "kindle_test1234",
        "idea": "test idea",
        "created_at": "2026-01-01T00:00:00+00:00",
        "status": "in_progress",
        "stages_completed": [],
    }
    (project_dir / "metadata.json").write_text(json.dumps(meta))
    return project_dir


