class TestPackageNodeHappyPath:
    """package_node() with a README generated by the agent."""

    @pytest.fixture()
    def project_dir(self, tmp_path: Path) -> Path:
        return _setup_project_dir(tmp_path)

    @pytest.fixture()
    def ui(self) -> MagicMock:
        return MagicMock()

    @patch("kindle.stages.package.run_agent", new_callable=AsyncMock)
    async def test_returns_package_readme_and_current_stage(
        self,
        mock_run_agent: AsyncMock,
        project_dir: Path,
        ui: MagicMock,
    ):
        """Return dict must contain the README content and stage name."""
        readme_content = "# Weather CLI\nA simple CLI weather app."
        # Simulate the agent writing README.md into the workspace
        (project_dir / "workspace" / "README.md").write_text(readme_content)
        mock_run_agent.return_value = _make_agent_result()

        result = await package_node(_make_state(str(project_dir)), ui)

        assert result["package_readme"] == readme_content
        assert result["current_stage"] == "package"

    @patch("kindle.stages.package.run_agent", new_callable=AsyncMock)
    async def test_saves_package_readme_artifact(
        self,
        mock_run_agent: AsyncMock,
        project_dir: Path,
        ui: MagicMock,
    ):
        """When README.md exists, it must be persisted as package_readme.md artifact."""
        readme_content = "# My Project\nSome docs."
        (project_dir / "workspace" / "README.md").write_text(readme_content)
        mock_run_agent.return_value = _make_agent_result()

        await package_node(_make_state(str(project_dir)), ui)

        artifact_path = project_dir / "artifacts" / "package_readme.md"
        assert artifact_path.exists()
        assert artifact_path.read_text() == readme_content

    @patch("kindle.stages.package.run_agent", new_callable=AsyncMock)
    async def test_marks_stage_complete(
        self,
        mock_run_agent: AsyncMock,
        project_dir: Path,
        ui: MagicMock,
    ):
        """mark_stage_complete must record 'package' in metadata."""
        mock_run_agent.return_value = _make_agent_result()

        await package_node(_make_state(str(project_dir)), ui)

        meta = json.loads((project_dir / "metadata.json").read_text())
        assert "package" in meta["stages_completed"]

    @patch("kindle.stages.package.run_agent", new_callable=AsyncMock)
    async def test_marks_project_done(
        self,
        mock_run_agent: AsyncMock,
        project_dir: Path,
        ui: MagicMock,
    ):
        """mark_project_done must set status to 'completed'."""
        mock_run_agent.return_value = _make_agent_result()

        await package_node(_make_state(str(project_dir)), ui)

        meta = json.loads((project_dir / "metadata.json").read_text())
        assert meta["status"] == "completed"
        assert "completed_at" in meta

    @patch("kindle.stages.package.run_agent", new_callable=AsyncMock)
    async def test_ui_lifecycle_calls(
        self,
        mock_run_agent: AsyncMock,
        project_dir: Path,
        ui: MagicMock,
    ):
        """UI must receive stage_start, deploy_complete, and stage_done."""
        mock_run_agent.return_value = _make_agent_result()

        await package_node(_make_state(str(project_dir)), ui)

        ui.stage_start.assert_called_once_with("package")
        ui.deploy_complete.assert_called_once_with(str(project_dir / "workspace"))
        ui.stage_done.assert_called_once_with("package")


